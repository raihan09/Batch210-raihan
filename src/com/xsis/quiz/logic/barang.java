package com.xsis.quiz.logic;

public class barang {
    private int kopi;
    private int gula;
    private int indomie;

    public int getKopi() {
        return kopi;
    }

    public void setKopi(int kopi) {
        this.kopi = kopi;
    }

    public int getGula() {
        return gula;
    }

    public void setGula(int gula) {
        this.gula = gula;
    }

    public int getIndomie() {
        return indomie;
    }

    public void setIndomie(int indomie) {
        this.indomie = indomie;
    }
}
